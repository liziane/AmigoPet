package amigopet;

public class Adotante {
    String nome;
    String cpf;
    String email;
    String endereco;
    String fone;
    
    public Adotante(String nome, String cpf, String email, String endereco, String fone){
        this.nome = nome;
        this.cpf = cpf;
        this.email = email;
        this.endereco = endereco;
        this.fone = fone;        
    }
}
